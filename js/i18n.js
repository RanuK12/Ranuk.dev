class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('ranuk-lang')
            || document.documentElement.lang
            || 'en';
        this.listeners = [];
    }

    init() {
        // If this page has no language switcher AND no data-i18n elements,
        // it's a single-language page (e.g. /ranuk-it/, /ranuk-it/ada/).
        // Respect its hardcoded <html lang> — don't override from localStorage.
        const hasSwitcher = document.querySelector('.lang-btn');
        const hasI18nNodes = document.querySelector('[data-i18n]');
        if (!hasSwitcher && !hasI18nNodes) {
            this.currentLang = document.documentElement.lang || this.currentLang;
            return;
        }

        // Sync button active states with stored language
        document.querySelectorAll('.lang-btn').forEach(btn => {
            const isActive = btn.dataset.lang === this.currentLang;
            btn.classList.toggle('active', isActive);
            btn.setAttribute('aria-pressed', String(isActive));
        });
        document.documentElement.lang = this.currentLang;
        this.applyTranslations();
        this.setupButtons();
    }

    setupButtons() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.dataset.lang;
                if (lang === this.currentLang) return;
                this.setLanguage(lang);
            });
        });
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('ranuk-lang', lang);

        document.querySelectorAll('.lang-btn').forEach(btn => {
            const isActive = btn.dataset.lang === lang;
            btn.classList.toggle('active', isActive);
            btn.setAttribute('aria-pressed', String(isActive));
        });

        document.documentElement.lang = lang;
        this.applyTranslations();
        this.listeners.forEach(fn => fn(lang));
    }

    applyTranslations() {
        const t = translations[this.currentLang];
        if (!t) return;

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            // Solo aplicar si la traducción NO está vacía. Antes (t[key] !== undefined) pisaba el
            // contenido con "" cuando faltaba la traducción ES -> BORRABA todo el texto (desastre
            // ranuk-it 06-24). Vacío = sin traducción -> dejar el texto original del HTML. (fix 06-24)
            if (t[key]) {
                el.innerHTML = t[key];
            }
        });

        // Soporte para placeholders dinámicos
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.dataset.i18nPlaceholder;
            if (t[key]) {
                el.placeholder = t[key];
            }
        });
    }

    get(key) {
        const t = translations[this.currentLang];
        const value = t ? t[key] : translations.en[key];

        // Fallback seguro: si el valor está vacío, usa EN. Si EN también está vacío, usa el key original.
        if (value === "") {
            const fallbackValue = translations.en[key];
            return fallbackValue !== "" ? fallbackValue : key;
        }

        return value || key;
    }

    getTypedStrings() {
        return this.get('typed') || translations.en.typed;
    }

    getCurrentLang() {
        return this.currentLang;
    }

    onChange(fn) {
        this.listeners.push(fn);
    }
}

window.i18n = new I18n();
